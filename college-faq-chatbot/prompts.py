@dataclass
class Prompts:
    """
    Container for all prompt templates used in the application.
    Prompts are loaded from files in the prompts/ directory.
    If a file doesn't exist, the default prompt is used.
    """
    grounding_prompt: str = field(default=DEFAULT_GROUNDING_PROMPT)
    generator_prompt: str = field(default=DEFAULT_GENERATOR_PROMPT)
    judge_prompt: str = field(default=DEFAULT_JUDGE_PROMPT)

    @classmethod
    def load(cls) -> "Prompts":
        """
        Load all prompts from files with fallback to defaults.

        Returns:
            Prompts: Instance with loaded prompt templates
        """
        prompt_files = {
            "grounding_prompt": PROMPTS_DIR / "grounding_prompt.txt",
            "generator_prompt": PROMPTS_DIR / "generator_prompt.txt",
            "judge_prompt": PROMPTS_DIR / "judge_prompt.txt",
        }

        loaded = {}
        for attr_name, filepath in prompt_files.items():
            loaded[attr_name] = cls._load_prompt_file(
                filepath,
                getattr(cls, attr_name, None)
            )

        return cls(**loaded)

    @staticmethod
    def _load_prompt_file(filepath: Path, default: str) -> str:
        """
        Load prompt from file, falling back to default.

        Args:
            filepath: Path to prompt text file
            default: Default prompt string if file doesn't exist

        Returns:
            str: Prompt template text
        """
        try:
            if filepath.exists():
                with open(filepath, "r", encoding="utf-8") as f:
                    content = f.read().strip()
                    logger.info(f"✅ Loaded prompt from: {filepath}")
                    return content
            else:
                logger.warning(
                    f"Prompt file not found: {filepath}. Using default."
                )
                return default
        except Exception as e:
            logger.error(f"Error loading prompt {filepath}: {e}. Using default.")
            return default

    def format_grounding_prompt(self, context: str, question: str) -> str:
        """
        Format the grounding prompt with context and question.

        Args:
            context: Retrieved document chunks
            question: User's question

        Returns:
            str: Formatted prompt
        """
        return self.grounding_prompt.format(context=context, question=question)

    def format_generator_prompt(self, context: str, question: str) -> str:
        """
        Format the generator prompt with context and question.

        Args:
            context: Retrieved document chunks
            question: User's question

        Returns:
            str: Formatted prompt
        """
        return self.generator_prompt.format(context=context, question=question)

    def format_judge_prompt(
        self,
        dimension: str,
        expected_answer: str,
        actual_answer: str,
        retrieved_context: str
    ) -> str:
        """
        Format the judge prompt for LLM evaluation.

        Args:
            dimension: Evaluation dimension (Functional, Quality, Safety, etc.)
            expected_answer: Expected/correct answer
            actual_answer: Chatbot's generated answer
            retrieved_context: Context chunks used for generation

        Returns:
            str: Formatted judge prompt
        """
        return self.judge_prompt.format(
            dimension=dimension,
            expected_answer=expected_answer,
            actual_answer=actual_answer,
            retrieved_context=retrieved_context
        )


def get_prompts() -> Prompts:
    """
    Get loaded prompts instance.

    Returns:
        Prompts: Loaded prompt templates
    """
    return Prompts.load()