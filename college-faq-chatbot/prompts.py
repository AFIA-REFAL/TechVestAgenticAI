"""
BVRIT Hyderabad College FAQ Chatbot - Prompt Management Module

Manages all prompt templates used throughout the application:
- Grounding Prompt: System instructions for the RAG chatbot
- Generator Prompt: Template for answer generation
- Judge Prompt: LLM Judge for evaluation comparison

Prompts are loaded from external .txt files with in-code defaults
as fallback, enabling easy modification without code changes.

Author: Senior GenAI Engineer
Version: 1.0.0
"""

import os
import logging
from pathlib import Path
from typing import Dict, Optional
from dataclasses import dataclass, field

logger = logging.getLogger(__name__)

PROMPTS_DIR = Path("prompts")

# ============================================================
# Default Prompt Templates (fallback if files don't exist)
# ============================================================

